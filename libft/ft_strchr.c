#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	char	ts;

	ts = c;
	while (*s)
	{
		if (*s == ts)
			return (s);
		s++;
	}
	if (*s == ts)
		return (s);
	return (NULL);
}
